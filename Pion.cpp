 void Pion::AfisareZar(TImage *Zar1, TImage *Zar2, TImage *Zar3, TImage *Zar4, TImage *Zar5, TImage *Zar6)
 {
        Zar1->Hide();
        Zar2->Hide();
        Zar3->Hide();
        Zar4->Hide();
        Zar5->Hide();
        Zar6->Hide();
        int zar=rand()%6+1;
        switch(zar)
        {case 1: Zar1->Show(); break;
        case 2: Zar2->Show(); break;
        case 3: Zar3->Show(); break;
        case 4: Zar4->Show(); break;
        case 5: Zar5->Show(); break;
        case 6: Zar6->Show(); break;  }
 }

 
